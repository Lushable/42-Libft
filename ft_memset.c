#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*str;
	size_t	i;

	i = 0;
	str = (char *)b;
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	return (b);
}

int main()
{
    char src[] = "mustafa";
    ft_memset(src, 48, 3);
    printf("%s", src);
}